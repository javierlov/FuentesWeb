		<td colspan="2" class="ContenidoSeccion">
			<p>Estas normas generan nuevas obligaciones para ART&#8217;s, canales de venta y empleadores:</p>
			<p>> <b>Cambios al proceso de afiliación:</b> nuevos formularios de solicitud de afiliación y contrato, cambios en la documentación adjunta, firma de Cliente y Vendedor, informatización del proceso de afiliación.</p>
			<p>> <b>Obligaciones de prevención:</b> al momento de afiliarse y luego una vez por año antes de que opere la renovación, el Empleador debe presentar un Formulario Relevamiento General de Riesgos Laborales (RGRL) por cada establecimiento (es DDJJ). Las ART&#8217;s deben visitar los establecimientos declarados y verificar la información contenida en los RGRL.</p>
			<p>> <b>Penalidades:</b> es falta grave de la ART si no está debidamente completado el Formulario de Afiliación; además será sancionada por el incumplimiento de cualquiera de los ítems de prevención. El empleador será sancionado con un 50% de aumento en la alícuota si no presenta el RGRL en tiempo y forma (la multa persiste hasta tanto subsane la situación), a partir del sexto mes posterior a la renovación de su contrato, según Res. SRT 771/09; el empleador tampoco podrá realizar el traspaso a otra ART.</p>
