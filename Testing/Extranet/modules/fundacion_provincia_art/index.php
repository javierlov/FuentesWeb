			<p>Esta definición convierte a la responsabilidad social en una de las bases de nuestro negocio. Entendemos que el buen funcionamiento de nuestra compañía está entrelazado con la salud de la sociedad y el bienestar general, ya que somos co-responsables del desarrollo económico y social de la comunidad en la que operamos.</p>
