			<p>Nuestra política de responsabilidad social puede resumirse como “poner a las personas en el centro de nuestros esfuerzos” y se despliega a través de dos grandes actividades: capacitación y concientización en salud y seguridad laboral a través de la Fundación Provincia ART y promoción de la inserción social de la persona con discapacidad a través del programa Integrando Realidades.</p>
