				<p>El estado de situación de pagos se encuentra sujeto a las condiciones generales de uso del sitio Web de Provincia ART S.A., a los presentes términos y condiciones particulares, a la Ley Nº 24.557 y normas reglamentarias. Provincia ART S.A. pone a disposición de los usuarios web el estado de cuenta dando cumplimiento a la Resolución SRT Nº 441/2006.</p>
