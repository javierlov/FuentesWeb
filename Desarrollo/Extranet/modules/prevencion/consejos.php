			<p style="margin-top: 0; margin-bottom: 0"><b>Elementos de protección personal</b></p>
			<p style="margin-top: 0; margin-bottom: 0">&gt; En caso de realizar una tarea que requiera protección visual, como la manipulación de productos químicos o la soldadura eléctrica, recuerde emplear anteojos protectores, pantallas transparentes o cualquier otro elemento que cumpla con las normas de seguridad.</p>
			<p style="margin-top: 0; margin-bottom: 0">&nbsp;</p>
			<p style="margin-top: 0; margin-bottom: 0"><b>Manejo de cargas</b></p>
			<p style="margin-top: 0; margin-bottom: 0">&gt; Para levantar pesos es importante mantener la espalda recta y flexionar bien las rodillas. De esta manera evitará dolores musculares y lesiones de espalda.</p>
			<p style="margin-top: 0; margin-bottom: 0">&nbsp;</p>
			<p style="margin-top: 0; margin-bottom: 0"><b>Prevención de incendios</b></p>
			<p style="margin-top: 0; margin-bottom: 0">&gt; Las causas más frecuentes de incendio son: sobrecarga eléctrica, colillas de cigarrillos mal apagadas y líquidos inflamables. Controlando y siendo conscientes de estos riesgos se pueden prevenir gran cantidad de incendios evitables.</p>
			<p style="margin-top: 0; margin-bottom: 0">&nbsp;</p>
			<p style="margin-top: 0; margin-bottom: 0">&gt; No todos los extintores sirven para combatir todos los tipos de fuegos. Asesórese para encontrar la clase de extintor indicado para la actividad específica en la que usted se desenvuelve.</p>
			<p style="margin-top: 0; margin-bottom: 0">&nbsp;</p>
			<p style="margin-top: 0; margin-bottom: 0"><b>Riesgo eléctrico</b></p>
			<p style="margin-top: 0; margin-bottom: 0">&gt; No utilice aparatos eléctricos ni manipule instalaciones eléctricas cuando se encuentren mojados, o si es usted quien tiene las manos o los pies húmedos.</p>
			<p style="margin-top: 0; margin-bottom: 0">&nbsp;</p>
			<p style="margin-top: 0; margin-bottom: 0"></p>
			<p style="margin-top: 0; margin-bottom: 0">&gt; Al manipular circuitos de instalaciones eléctricas, no altere ni modifique el funcionamiento de los dispositivos de seguridad, como por ejemplo interruptores automáticos contra sobre intensidad o aumento de temperatura.</p>
			<p style="margin-top: 0; margin-bottom: 0">&nbsp;</p>
			<p style="margin-top: 0; margin-bottom: 0">&gt; No utilice cables dañados, fichas de enchufe resquebrajadas, ni artefactos cuya carcaza presente desperfectos.</p>
			<p style="margin-top: 0; margin-bottom: 0">&nbsp;</p>
			<p style="margin-top: 0; margin-bottom: 0"><b>Manipulación de sustancias químicas</b></p>
			<p style="margin-top: 0; margin-bottom: 0">&gt; A la hora de manipular productos químicos, es fundamental que los mismos tengan colocadas las etiquetas correspondientes. Éstas deberán indicar la clase de riesgo a la que se está expuesto, el nombre del producto y quién es el fabricante.</p>
			<p style="margin-top: 0; margin-bottom: 0">&nbsp;</p>
			<p style="margin-top: 0; margin-bottom: 0">&gt; Los gases comprimidos deben contar con una etiqueta identificatoria de contenido en su envase, ser almacenados en posición vertical, ser ubicados en sectores diferenciados, mantenerlos siempre con el capuchón de seguridad colocado y estar sujetados para evitar posibles caídas.</p>
			<p style="margin-top: 0; margin-bottom: 0">&nbsp;</p>
			<p style="margin-top: 0; margin-bottom: 0"><b>Trabajo agropecuario</b></p>
			<p style="margin-top: 0; margin-bottom: 0">&gt; Si usted trabaja en una planta de silos, nunca trabaje solo ni 	desconecte las puestas a tierra. Recuerde utilizar cinturón de seguridad para escalar norias.</p>
			<p style="margin-top: 0; margin-bottom: 0">&nbsp;</p>
			<p style="margin-top: 0; margin-bottom: 0">&gt; Si usted trabaja con animales: no utilice jeringas defectuosas, use guantes hasta el codo para ayudar en los partos y cuide sanitariamente a todos sus animales.</p>
			<p style="margin-top: 0; margin-bottom: 0">&nbsp;</p>
			<p style="margin-top: 0; margin-bottom: 0">&gt; Si usted conduce un tractor, manténgalo en condiciones, use el cinturón de seguridad, no cruce por encima de la toma de fuerza y por sobre todo evite transportar personas en el tractor.</p>
