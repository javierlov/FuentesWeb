			<p><img border="0" src="/modules/acerca_de_provincia_art/images/intro.jpg"></p>
			<p>Provincia ART es una de las empresas aseguradoras del <b>Grupo Provincia</b>, especializada en la prestación del seguro de cobertura de riesgos del trabajo.</p>
			<p>Tras la sanción de la Ley de Riesgos del Trabajo, el Banco Provincia renovó su compromiso con el apoyo al trabajo argentino a través de la creación de una aseguradora capaz de brindar todos los servicios y prestaciones requeridos por la nueva legislación.</p>
			<p>Abrimos nuestras puertas en 1996, comprometidos con la misión de contribuir a la creación de un ambiente de trabajo más sano, seguro y productivo en entidades públicas y privadas de envergadura grande, mediana y pequeña.</p>
			<p>Actualmente, la participación mayoritaria de nuestro capital accionario corresponde al Grupo Provincia, el cual brinda la solidez y el prestigio propio de su liderazgo en materia de negocios complementarios de los servicios del Banco Provincia, en todo el territorio nacional.</p>
			<p>Desde nuestra creación, hemos mantenido el liderazgo del mercado en cantidad de trabajadores a los que presta servicios. Nuestro liderazgo también está expresado en el profesionalismo y la efectividad del equipo de prevención, así como también en el compromiso con el bienestar los trabajadores accidentados y sus familias.</p>
