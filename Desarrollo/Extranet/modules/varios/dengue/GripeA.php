				<td class="ContenidoSeccion">		
				<p>El virus de la gripe H1N1, se transmite principalmente por vía respiratoria, mediante gotitas expulsadas del aparato respiratorio o suspendidas en el aire, que entran en contacto con las mucosas de las vías respiratorias altas o conjuntivas. La transmisión por contacto de manos es menos intensa, pero también debemos tenerla en cuenta. </p>
				<b>> SÍNTOMAS</b><br>
				Sus comienzos son como una gripe común:</p>
				&#8226; Fiebre por encima de los 38º.<br>
				&#8226; Mialgias, dolores articulares y cefaleas.<br>
				&#8226; Dolor de garganta.<br>
				&#8226; Tos.<br>
				&#8226; Dificultades respiratorias, disnea.<br>
				&#8226; Mucosidad.<br>
				&#8226; Decaimiento general.</p>
				
				<b>> RECOMENDACIONES PARA EVITAR EL CONTAGIO</b><br>
				La expansión de la Gripe A puede combatirse adoptando medidas vinculadas con la higiene personal, en el hogar y en el lugar de trabajo.</p>
				&#8226; Al toser y estornudar, cubrirse la boca y la nariz con pañuelo descartable o con la articulación interna del codo.</p>
				&#8226; Lavarse las manos frecuentemente con agua y jabón (especialmente en niños).</p>
				&#8226; Mantener higienizados picaportes y objetos de uso común.</p>
				&#8226; Evitar saludar con besos y dando la mano.</p>
				&#8226; Evitar compartir alimentos, vasos o cubiertos.</p>
				&#8226; Ventilar la casa, las oficinas y lugares cerrados.</p>
				&#8226; Limpiar frecuentemente la cocina, el baño, teléfonos y objetos de uso común así como los juguetes de los niños con detergentes o desinfectantes como hipoclorito de sodio o alcohol.</p>
				&#8226; Evitar en lo posible lugares muy concurridos (subte, auditorios, escuelas, iglesias, etc.).</p>
				&#8226; Mantenerse alejados de las personas con infecciones respiratorias.</p>
				&#8226; En oficinas, call centers y cibercafés se recomienda limpiar teclados y ratones con alcohol para desinfectar y evitar la propagación del virus.</p>
				&#8226; Recuerde que el uso de barbijo no previene el contagio en la calle ni en los transporte públicos. Sólo deben ser usados por enfermos y personal de salud.</p>
				&#8226; Abrigarse y evitar cambios bruscos de temperatura.</p>
				<b>> VACUNACIÓN</b><br>
				Los grupos a vacunar se han definido según recomendaciones de la Comisión Nacional de Inmunizaciones:</p>
				&#8226; Trabajadores de salud en general, y aquellos que presten cuidado a pacientes o personal de laboratorio que esté en contacto con muestras que contengan secreciones respiratorias.</p>
				&#8226; Embarazadas y puérperas hasta 6 meses postparto.</p>
				&#8226; Niños de 6 meses a 4 años.</p>
				&#8226; Niños y adultos de 5 a 64 años inclusive, que presenten: antecedentes de riesgo como obesidad mórbida, transplantados, pacientes oncológicos, síndromes genéticos, enfermedades respiratorias crónicas, inmuno deprimidos, cardiopatías, diabéticos.</p>
				&#8226; Adultos a partir de 65 años independiente de factores de riesgo.</p>
				La vacuna para la gripe pandémica H1N1 &#8220;FOCETRIA&#8221; (Monovalente), será provista por el Estado Argentino y la vacunación se llevará a cabo en Hospitales Públicos o Centros Vacunatorios. Para consultar los lugares de vacunación cercanos a su residencia debe comunicarse al 0800-222-1002 o consultar a su médico de cabecera.</p>
				En caso de presentar un cuadro de fiebre alta de manera repentina, tos, dolor de cabeza, muscular y de articulaciones, se deberá de acudir de inmediato al médico o a la unidad de salud, preferentemente antes de las 48 hs.</p>
