		<td class="ContenidoSeccion">		
			<p>
				<font color="#00539B">></font> <b>¿Qué es el dengue?</b><br />
				Es una enfermedad viral trasmitida por la picadura del mosquito Aedes aegypti, también portador de la fiebre amarilla. Mide de 3 a 5 mm. con la particularidad de ser de color negro y presentar manchas blancas en el lomo y en las patas. El mosquito adulto vive 30 días y su habitat natural es tropical o subtropical. El dengue es de carácter endémico: se encuentra principalmente en el norte de Argentina y desde allí se disemina hacia el resto del país, inclusive la Provincia de Buenos Aires.
			</p>
			<p>
				<font color="#00539B">></font> <b>¿Cómo se expande el virus?</b><br />
				1- El mosquito pica a una persona infectada y se contagia.<br>
				2- El mosquito infectado transmite la enfermedad a personas sanas.<br>
				3- Si otro mosquito sano chupa la sangre infectada, se repite la cadena.
			</p>
			<p><font color="#00539B">></font> <b>¿Qué hacer?</b> (medidas a tener en cuenta)<br></p>
			<p>
				<b>A nivel personal</b><br />
				&#8226; Usar vestimenta que cubra la mayor parte del cuerpo (camisas o remeras de manga larga y pantalones largos de colores claros.<br />
				&#8226; En exteriores recuerde aplicar repelente de insectos (crema a base de agua o líquidos) en las partes del cuerpo que queden al descubierto. Tener cuidado en su aplicación en niños.<br />
				&#8226; En ambientes cerrados utilizar espirales, pastillas o líquidos termoevaporables. Reforzar la limpieza de las casas.
			</p>
			<p>
				<b>A nivel comunitario</b><br />
				&#8226; Mantener convenientemente tapados los barriles, tanques, depósitos, etc. que sean utilizados como reservas de agua. Recuerde renovar regular y diariamente el agua de los bebederos de animales, fuentes, floreros, etc.<br />
				&#8226; Eliminar todo recipiente inservible o deshecho que se encuentre al aire libre y que pueda almacenar y estancar agua de lluvia (neumáticos, latas, botellas, baldes, etc.), dado que allí pone los huevos el mosquito.<br />
				&#8226; Colocar telas mosquiteros en las aberturas de los lugares de trabajos y en las viviendas.<br />
				&#8226; Asegurar el drenaje de agua ejecutando acciones de limpieza en canaletas y recodos; destape de cañerías de desague pluvial a través de rejillas y realizar cualquier otra actividad que sirva para evitar el estancamiento de agua.
			</p>
