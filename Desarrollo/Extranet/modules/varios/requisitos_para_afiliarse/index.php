		<td class="ContenidoSeccion">
			Aquí encontrará toda la información y pasos necesarios para afiliarse a Provincia ART. Recuerde que para completar el proceso de afiliación deberá presentar la documentación requerida por normativa de la Superintendencia de Riesgos del Trabajo (S.R.T.) y completar todos los campos de los formularios, sin excepción.
			<p><b>Para iniciar el proceso de afiliación complete y presente la siguiente documentación:</b></p>
			<p>> Completar la <b>Solicitud de Afiliación</b>, confeccionada por un representante de Provincia ART.</p>
			<p>> Si la empresa cuenta con más de un local, filial o lugar de riesgo (establecimientos), completar el formulario de <b><a class="linkSubrayado" href="/modules/servicio/archivos/Provincia_ART_Ubicacion_riesgo.pdf" target="_blank">Ubicación de Riesgo</a></b>.</p>
			<p>> Presentar las <a class="linkSubrayado" target="_blank" href="http://www.afip.gov.ar/">constancias de<b> Alta temprana de la A.F.I.P. - Mi simplificación</b></a><b>.</b> Si la empresa posee más de 35 trabajadores, adjuntar el listado que surge de la página web de A.F.I.P. con los movimientos realizados por “Mi simplificación”.</p>
			<p>> Si es una persona física, deberá presentar <b>copia del D.N.I. del titular, firmada por el titular y el representante de Provincia ART</b>.</p>
			<p>> Si es una persona jurídica, deberá presentar <b>copias del poder de quien solicita la cobertura, su D.N.I. y el estatuto social, todas firmadas por el titular y el representante de Provincia ART</b>.</p>
			<p>> Presentar fotocopia del formulario 460 de <b>constancia de inscripción en la A.F.I.P.</b></p>
			<p>> Completar los <b>formularios de RGRL</b> (Resolución 463/09 y 529/09), en función de sus establecimientos y la actividad de cada uno de ellos.</p>
			<p>&nbsp;</td>
