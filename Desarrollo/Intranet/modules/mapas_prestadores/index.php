					<p style="color:#00539B"><b>¿QUÉ ES?</b></p>
					<p>Un mapa realizado en la plataforma Google Maps que localiza los prestadores de: Prestadores de Traslado, Auditores, Terapistas Ocupacionales, Farmacia, Fisio Kinesio Terapia (FKT), Centros con Internación, Centros Ambulatorios, Tomografías Axial Computada (TAC), Resonancia Magnética Nuclear (RMN) y Hospitales. La información es proveída por el área de Prestadores y actualizada periódicamente.</p>
					<p>&nbsp;</p>
					<p style="color:#00539B"><b>¿PARA QUÉ SIRVE?</b></p>
					<p>Esta herramienta permite a los usuarios acceder a la ubicación geográfica de los principales prestadores de la ART. A partir de esta información se pueden optimizar las derivaciones y autorizaciones priorizando la proximidad al domicilio del siniestrado.</p>
					<p>&nbsp;</p>
					<p style="color:#00539B"><b>¿CÓMO FUNCIONA?</b></p>
					<p>Los usuarios deben ingresar la dirección del siniestrado en la barra de búsqueda, un ícono de color verde determinará su localización y una serie de íconos (ver referencias) mostrarán los prestadores que se encuentran en las cercanías. A partir de esta información se realiza la derivación/autorización minimizando así las distancias. Por ejemplo en la Imagen 1 se ingresó “Carlos Pellegrini 91” en la barra de búsqueda (lupa).</p>
